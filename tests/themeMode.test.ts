assert.match(style, /html\[data-app-theme="win11Dark"\]/);
assert.match(style, /html\[data-app-theme="macosLight"\]/);
assert.match(style, /html\[data-app-theme="macosDark"\]/);
assert.match(style, /--main-bg:\s*#202020;/);
assert.match(style, /--clipboard-card-bg:\s*rgba\(255, 255, 255, 0\.78\);/);
assert.match(rootTheme, /--main-bg:\s*#0b100e;/);
assert.match(rootTheme, /--main-bg-soft:\s*#151d18;/);
assert.match(rootTheme, /--panel-bg:\s*rgba\(27, 36, 31, 0\.92\);/);
assert.match(rootTheme, /--theme-accent:\s*#35c85a;/);
